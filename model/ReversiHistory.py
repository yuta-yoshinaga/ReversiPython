import ReversiPoint

# ////////////////////////////////////////////////////////////////////////////////
# ///	@class		ReversiHistory
# ///	@brief		リバーシ履歴クラス
# ///
# ////////////////////////////////////////////////////////////////////////////////
class ReversiHistory:

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			コンストラクタ
	# ///	@fn				__init__(self)
	# ///	@return			ありません
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2014.07.23
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def __init__(self):
		self.__point = ReversiPoint.ReversiPoint()
		self.__color = 0
		self.reset()

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			ゲッター
	# ///	@fn				get_point(self)
	# ///	@param[in]		self
	# ///	@return			ReversiPoint point
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def get_point(self):
		return self.__point

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			セッター
	# ///	@fn				set_point(self, point)
	# ///	@param[in]		self
	# ///	@param[in]		point
	# ///	@return			ありません
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def set_point(self, point):
		self.__point = point

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			ゲッター
	# ///	@fn				get_color(self)
	# ///	@param[in]		self
	# ///	@return			color
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def get_color(self):
		return self.__color

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			セッター
	# ///	@fn				set_color(self, color)
	# ///	@param[in]		self
	# ///	@param[in]		color
	# ///	@return			ありません
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def set_color(self, color):
		self.__color = color

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			リセット
	# ///	@fn				reset(self)
	# ///	@param[in]		self
	# ///	@return			ありません
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2014.07.23
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def reset(self):
		self.__point.x = -1
		self.__point.y = -1
		self.__color = -1

	point = property(get_point, set_point)
	color = property(get_color, set_color)

