class ReversiPoint:

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			コンストラクタ
	# ///	@fn				public ReversiPoint()
	# ///	@return			ありません
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def __init__(self):
		self.__x = 0
		self.__y = 0

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			ゲッター
	# ///	@fn				getX()
	# ///	@return			x
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def get_x(self):
		return self.__x

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			セッター
	# ///	@fn				setX(x)
	# ///	@param[in]		x
	# ///	@return			ありません
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def set_x(self, x):
		self.__x = x

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			ゲッター
	# ///	@fn				getY()
	# ///	@return			y
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def get_y(self):
		return self.__y

	# ////////////////////////////////////////////////////////////////////////////////
	# ///	@brief			セッター
	# ///	@fn				setY(y)
	# ///	@param[in]		y
	# ///	@return			ありません
	# ///	@author			Yuta Yoshinaga
	# ///	@date			2018.04.01
	# ///
	# ////////////////////////////////////////////////////////////////////////////////
	def set_y(self, y):
		self.__y = y

	x = property(get_x, set_x)
	y = property(get_y, set_y)

